public class Pro2_All_PrimitiveData_Types {

	public static void main(String[] args) 
	{
		/*Byte value: 10
		Short value: 100
		Int value: 5000
		Long value: 9000000000
		Float value: 5.5
		Double value: 99.99
		Char value: A
		Boolean value: true*/
		byte value=10;
		System.out.println("Byte value:"+" "+value);
		short value1=100;
		System.out.println("Short value:"+" "+value1);
		int value2=5000;
		System.out.println("Int value:"+" "+value2);
		long value3=9000000000L;
		System.out.println("Long value:"+" "+value3);
		float value4=5.5F;
		System.out.println("Float value:"+" "+value4);
		double value5=99.99;
		System.out.println("Float value:"+" "+value5);
		char value6='A';
		System.out.println("Char value:"+" "+value6);
		boolean istrue=true;
		System.out.println("Boolean value:"+" "+istrue);
		

	}

}
